class LZHUFDecoder:
    def __init__(self, data: bytes):
        self.data = data
        self.pos = 0
        self.getbuf = 0
        self.getlen = 0
        self.text_buf = bytearray([0x20]) * (N + F - 1)
        self.r = N - F
        self.freq = [0]*(T+1)
        self.prnt = [0]*(T+N_CHAR)
        self.son = [0]*T
        self._start_huff()

    def _fillbuf(self):
        # Matches the confirmed binary exactly: both GetBit and GetByte
        # refill whenever bitcount < 9 (not "< however many bits I need now").
        while self.getlen < 9:
            c = self.data[self.pos] if self.pos < len(self.data) else 0
            self.pos += 1
            self.getbuf |= c << (8 - self.getlen)
            self.getlen += 8

    def _getbit(self):
        self._fillbuf()
        b = (self.getbuf >> 15) & 1
        self.getbuf = (self.getbuf << 1) & 0xffff
        self.getlen -= 1
        return b

    def _getbyte(self):
        self._fillbuf()
        b = (self.getbuf >> 8) & 0xff
        self.getbuf = (self.getbuf << 8) & 0xffff
        self.getlen -= 8
        return b

    def _start_huff(self):
        for i in range(N_CHAR):
            self.freq[i] = 1
            self.son[i] = i + T
            self.prnt[i + T] = i
        i = 0
        j = N_CHAR
        while j <= R:
            self.freq[j] = self.freq[i] + self.freq[i+1]
            self.son[j] = i
            self.prnt[i] = j
            self.prnt[i+1] = j
            i += 2
            j += 1
        self.freq[T] = 0xffff
        self.prnt[R] = 0

    def _reconst(self):
        j = 0
        for i in range(T):
            if self.son[i] >= T:
                self.freq[j] = (self.freq[i] + 1) // 2
                self.son[j] = self.son[i]
                j += 1
        i = 0
        for j in range(N_CHAR, T):
            k = i + 1
            f = self.freq[i] + self.freq[k]
            self.freq[j] = f
            k2 = j - 1
            while f < self.freq[k2]:
                k2 -= 1
            k2 += 1
            self.freq[k2+1:j+1] = self.freq[k2:j]
            self.freq[k2] = f
            self.son[k2+1:j+1] = self.son[k2:j]
            self.son[k2] = i
            i += 2
        for i in range(T):
            k = self.son[i]
            if k >= T:
                self.prnt[k] = i
            else:
                self.prnt[k] = i
                self.prnt[k+1] = i

    def _update(self, c):
        if self.freq[R] == MAX_FREQ:
            self._reconst()
        c = self.prnt[c + T]
        while True:
            self.freq[c] += 1
            k = self.freq[c]
            l = c + 1
            if l < T and self.freq[l] < k:
                while l + 1 < T and self.freq[l+1] < k:
                    l += 1
                self.freq[c] = self.freq[l]
                self.freq[l] = k
                i = self.son[c]
                self.prnt[i] = l
                if i < T:
                    self.prnt[i+1] = l
                j = self.son[l]
                self.son[l] = i
                self.prnt[j] = c
                if j < T:
                    self.prnt[j+1] = c
                self.son[c] = j
                c = l
            c = self.prnt[c]
            if c == 0:
                break

    def _decode_char(self):
        c = self.son[R]
        while c < T:
            c += self._getbit()
            c = self.son[c]
        c -= T
        self._update(c)
        return c

    def _decode_position(self):
        i = self._getbyte()
        c = d_code[i] << 6
        j = d_len[i] - 2
        while j > 0:
            i = ((i << 1) & 0xffffffff) + self._getbit()
            j -= 1
        return c | (i & 0x3f)

    def decode(self, outsize):
        out = bytearray()
        r = self.r
        text_buf = self.text_buf
        count = 0
        while count < outsize:
            c = self._decode_char()
            if c < 256:
                out.append(c)
                text_buf[r] = c
                r = (r + 1) % N
                count += 1
            else:
                i = (r - self._decode_position() - 1) % N
                j = c - 255 + THRESHOLD
                for _ in range(j):
                    if count >= outsize:
                        break
                    cc = text_buf[i % N]
                    out.append(cc)
                    text_buf[r] = cc
                    r = (r + 1) % N
                    i += 1
                    count += 1
        self.r = r
        return bytes(out)


def decode_lzhuf(compressed: bytes, outsize: int) -> bytes:
    return LZHUFDecoder(compressed).decode(outsize)
