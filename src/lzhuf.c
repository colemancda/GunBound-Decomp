typedef struct {
    const uint8_t *data;
    size_t data_len;
    size_t pos;
    uint32_t getbuf;
    uint32_t getlen;
    uint8_t text_buf[N + F - 1];
    int r;
    uint16_t freq[T + 1];
    int prnt[T + N_CHAR];
    int son[T];
} lzhuf_state;

static void fillbuf(lzhuf_state *s) {
    /* Matches the confirmed binary exactly: both GetBit and GetByte
     * refill whenever bitcount < 9, not "< however many bits needed now". */
    while (s->getlen < 9) {
        uint8_t c = (s->pos < s->data_len) ? s->data[s->pos] : 0;
        s->pos++;
        s->getbuf |= (uint32_t)c << (8 - s->getlen);
        s->getlen += 8;
    }
}

static int getbit(lzhuf_state *s) {
    fillbuf(s);
    int b = (s->getbuf >> 15) & 1;
    s->getbuf = (s->getbuf << 1) & 0xffff;
    s->getlen -= 1;
    return b;
}

static int getbyte(lzhuf_state *s) {
    fillbuf(s);
    int b = (s->getbuf >> 8) & 0xff;
    s->getbuf = (s->getbuf << 8) & 0xffff;
    s->getlen -= 8;
    return b;
}

static void start_huff(lzhuf_state *s) {
    int i, j;
    for (i = 0; i < N_CHAR; i++) {
        s->freq[i] = 1;
        s->son[i] = i + T;
        s->prnt[i + T] = i;
    }
    i = 0;
    j = N_CHAR;
    while (j <= R) {
        s->freq[j] = s->freq[i] + s->freq[i + 1];
        s->son[j] = i;
        s->prnt[i] = j;
        s->prnt[i + 1] = j;
        i += 2;
        j++;
    }
    s->freq[T] = 0xffff;
    s->prnt[R] = 0;
}

static void reconst(lzhuf_state *s) {
    int i, j, k, f, k2;

    j = 0;
    for (i = 0; i < T; i++) {
        if (s->son[i] >= T) {
            s->freq[j] = (s->freq[i] + 1) / 2;
            s->son[j] = s->son[i];
            j++;
        }
    }
    i = 0;
    for (j = N_CHAR; j < T; j++) {
        k = i + 1;
        f = s->freq[i] + s->freq[k];
        s->freq[j] = (uint16_t)f;
        k2 = j - 1;
        while (f < s->freq[k2]) {
            k2--;
        }
        k2++;
        memmove(&s->freq[k2 + 1], &s->freq[k2], (size_t)(j - k2) * sizeof(s->freq[0]));
        s->freq[k2] = (uint16_t)f;
        memmove(&s->son[k2 + 1], &s->son[k2], (size_t)(j - k2) * sizeof(s->son[0]));
        s->son[k2] = i;
        i += 2;
    }
    for (i = 0; i < T; i++) {
        k = s->son[i];
        if (k >= T) {
            s->prnt[k] = i;
        } else {
            s->prnt[k] = i;
            s->prnt[k + 1] = i;
        }
    }
}

static void update(lzhuf_state *s, int c) {
    if (s->freq[R] == MAX_FREQ) {
        reconst(s);
    }
    c = s->prnt[c + T];
    for (;;) {
        s->freq[c]++;
        int k = s->freq[c];
        int l = c + 1;
        if (l < T && s->freq[l] < (uint16_t)k) {
            while (l + 1 < T && s->freq[l + 1] < (uint16_t)k) {
                l++;
            }
            s->freq[c] = s->freq[l];
            s->freq[l] = (uint16_t)k;

            int i = s->son[c];
            s->prnt[i] = l;
            if (i < T) {
                s->prnt[i + 1] = l;
            }
            int jj = s->son[l];
            s->son[l] = i;
            s->prnt[jj] = c;
            if (jj < T) {
                s->prnt[jj + 1] = c;
            }
            s->son[c] = jj;
            c = l;
        }
        c = s->prnt[c];
        if (c == 0) {
            break;
        }
    }
}

static int decode_char(lzhuf_state *s) {
    int c = s->son[R];
    while (c < T) {
        c += getbit(s);
        c = s->son[c];
    }
    c -= T;
    update(s, c);
    return c;
}

static int decode_position(lzhuf_state *s) {
    int i = getbyte(s);
    int c = d_code[i] << 6;
    int j = d_len[i] - 2;
    while (j > 0) {
        i = (i << 1) + getbit(s);
        j--;
    }
    return c | (i & 0x3f);
}

uint8_t *lzhuf_decode(const uint8_t *compressed, size_t compressed_size,
                       size_t outsize) {
    lzhuf_state *s = calloc(1, sizeof(lzhuf_state));
    if (!s) {
        return NULL;
    }
    uint8_t *out = malloc(outsize > 0 ? outsize : 1);
    if (!out) {
        free(s);
        return NULL;
    }

    s->data = compressed;
    s->data_len = compressed_size;
    memset(s->text_buf, 0x20, sizeof(s->text_buf));
    s->r = N - F;
    start_huff(s);

    size_t count = 0;
    int r = s->r;
    while (count < outsize) {
        int c = decode_char(s);
        if (c < 256) {
            out[count++] = (uint8_t)c;
            s->text_buf[r] = (uint8_t)c;
            r = (r + 1) % N;
        } else {
            int i = (r - decode_position(s) - 1 + N) % N;
            int j = c - 255 + THRESHOLD;
            for (int n = 0; n < j; n++) {
                if (count >= outsize) {
                    break;
                }
                uint8_t cc = s->text_buf[i % N];
                out[count++] = cc;
                s->text_buf[r] = cc;
                r = (r + 1) % N;
                i++;
            }
        }
    }
    s->r = r;

    free(s);
    return out;
}
